package com.github.rguliamov.dreamtrip.app.infra.util.helperClasses;

/**
 * @author Guliamov Rustam
 */
public class B {
    private int age;

    private String name;

    private int id;

    public B() {
    }

    public B(int age, String name, int id) {
        this.age = age;
        this.name = name;
        this.id = id;
    }

    public int getAge() {
        return age;
    }

    public String getName() {
        return name;
    }

    public int getId() {
        return id;
    }
}
